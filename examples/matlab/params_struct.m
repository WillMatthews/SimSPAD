function params = params_struct(config)
%PARAMS_STRUCT maps a MATLAB config struct to the SimSPAD parameter schema.
% The field names and order match SiPM::dump_configuration() / the .json schema
% used by both the simspad CLI and the web server. digital_thresholds is an
% array in the config; the simulator uses a single scalar threshold.

params = struct( ...
    'dt',               config.dt, ...
    'numMicrocell',     config.num_microcell, ...
    'vBias',            config.vbias, ...
    'vBr',              config.vbr, ...
    'tauRecovery',      config.recovery, ...
    'pdeMax',           config.pde_Max, ...
    'vChr',             config.pde_vchr, ...
    'cCell',            config.ccell, ...
    'tauFwhm',          config.pulse_fwhm, ...
    'digitalThreshold', config.digital_thresholds(1));

end
